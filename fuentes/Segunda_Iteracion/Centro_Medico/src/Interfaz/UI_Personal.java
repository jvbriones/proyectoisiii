    /*Comprueba que la fecha introducida es correcta
     * 
     *@param persona, string que indica si se trata del formulario de Paciente o Personal
     *@return booleano diciendo si es v√°lido o no
     */
    private boolean compruebaFecha(String persona){
        
        int dia = 0;
        int mes = 0;
        int anio = 0;
        int diaActual;
        int mesActual;
        int anioActual;

        if(persona.equals("GestionarPaciente")){
            dia = Integer.parseInt(jTextFieldFechaNacimientoDiaPersonal.getText());
            mes = Integer.parseInt(jTextFieldFechaNacimientoMesersonal.getText());
            anio = Integer.parseInt(jTextFieldFechaNacimientoAnioPersonal.getText());
        }


        Calendar fechaActual = Calendar.getInstance();
        diaActual = fechaActual.get(Calendar.DATE);
        mesActual = fechaActual.get(Calendar.MONTH);
        anioActual = fechaActual.get(Calendar.YEAR);


        /**Si se sale de los rangos*/
        if((dia <= 0)
                || (dia > 31)
                || (mes <= 0)
                || (mes > 12)
                || (anio < 1900)
                || (anio > anioActual) ){
            return false;
        }

        /**Si ha nacido en el a√±o actual, hay que ver que el mes no sea mayor que el actual*/
        /**Si ha nacido en el a√±o y mes actual, hay que comprobar que el dia sea menor que el actual*/
        if(anio == anioActual){
            if(mes > mesActual){
                return false;
            } else{
                if(dia > diaActual){
                    return false;
                }
            }
        }

        /**Si estamos en un mes que tiene 30 d√≠as o 29 en el caso de febrero, hay que comprobar que no se pase*/
        if(mes%2 == 0){
            if((mes == 2) && (dia >29)){
                return false;
            }else{
                if(dia > 30){
                    return false;
                }
            }
        }

        return true;

    }


