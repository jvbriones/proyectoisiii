    private void jTextFieldDNIAtenderPacienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldDNIAtenderPacienteKeyTyped
        // TODO add your handling code here:
         /**Limitamos a que el DNI siempre sea correcto sintácticamente*/
        char teclaPresionada = evt.getKeyChar();

        if(jTextFieldDNIAtenderPaciente.getText().length() < 8)
            if(Character.isDigit(teclaPresionada) == false)
                evt.consume();

        if(jTextFieldDNIAtenderPaciente.getText().length() == 8)
            if(Character.isDigit(teclaPresionada) == true){
                evt.consume();
                }else if(Character.isLowerCase(teclaPresionada)){
                    jTextFieldDNIAtenderPaciente.setText(jTextFieldDNIAtenderPaciente.getText().toUpperCase());
                }

        if(jTextFieldDNIAtenderPaciente.getText().length() > 8)
            evt.consume();

    }//GEN-LAST:event_jTextFieldDNIAtenderPacienteKeyTyped

