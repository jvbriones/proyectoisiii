    /*Comprueba que la fecha introducida es correcta
     * 
     *@param persona, string que indica si se trata del formulario de Paciente o Personal
     *@return booleano diciendo si es v√°lido o no
     */
    
private boolean compruebaFecha(String persona){

        int dia = 0;
        int mes = 0;
        int anio = 0;
        int diaActual;
        int mesActual;
        int anioActual;
        Calendar fecha;
        if(persona.equals("GestionarPersonal")){
           fecha=dateChooserCombo2.getSelectedDate();
           dia=fecha.get(Calendar.DATE);
           mes=fecha.get(Calendar.MONTH)+1;
           anio=fecha.get(Calendar.YEAR);
        }

         Date fechaa= new Date();
         diaActual=fechaa.getDate();
         mesActual=fechaa.getMonth()+1;
         anioActual=fechaa.getYear()+1901;


        /**Si se sale de los rangos*/
        if((dia <= 0)
                || (dia > 31)
                || (mes <= 0)
                || (mes > 12)
                || (anio < 1900)
                || (anio > anioActual) ){
            return false;
        }

        /**Si ha nacido en el a√±o actual, hay que ver que el mes no sea mayor que el actual*/
        /**Si ha nacido en el a√±o y mes actual, hay que comprobar que el dia sea menor que el actual*/
        if(anio == anioActual){
            if(mes > mesActual){
                return false;
            } else{
                if(dia > diaActual){
                    return false;
                }
            }
        }

        /**Si estamos en un mes que tiene 30 d√≠as o 29 en el caso de febrero, hay que comprobar que no se pase*/
        if(mes%2 == 0){
            if((mes == 2) && (dia >29)){
                return false;
            }else{
                if(dia > 30){
                    return false;
                }
            }
        }

        return true;

    }


